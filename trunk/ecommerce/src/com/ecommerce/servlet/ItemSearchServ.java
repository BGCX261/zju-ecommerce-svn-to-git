	protected void service(HttpServletRequest request, HttpServletResponse response)
	throws ServletException, IOException {
// TODO Auto-generated method stub
		String itemName=request.getParameter("keyword");
		String itemCategory=request.getParameter("classification");
		System.out.println(itemName+itemCategory);
		ItemDAO query=new ItemDAO();
		ArrayList items = new ArrayList();
		items = (ArrayList)query.findByNameAlike(itemName);
		String target="";		
		for(int i=0; i<items.size();i++){
			Item t = (Item)items.get(i);
			System.out.println(t.getItemName()+t.getPrice());
		}
		target="./merchandises.jsp";
		HttpSession session = request.getSession();
		session.setAttribute("items",items);
		session.setAttribute("searchItemName", itemName);
		request.getRequestDispatcher(target).forward(request, response);
	}
